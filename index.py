import json
import re

import requests
import wget
from bs4 import BeautifulSoup

from createPlaylist import create_playlist, get_tracks, get_playlist_id, add_tracks_to_playlist

# URL = 'https://music.apple.com/ng/playlist/thetaragold/pl.u-76oNkyeIW1ag0G0'
URL = 'https://music.apple.com/ng/playlist/ambient-chill-music/pl.u-XkD0vlLU2Vx1qYW'
page = requests.get(URL)

soup = BeautifulSoup(page.content, 'html.parser')

results = soup.find(attrs={"name": "schema:musicPlaylist", "type": "application/ld+json"})
song_results = soup.find_all(attrs={"class": "col col-song"})
playlist_cover = soup.find(attrs={"class": "media-artwork-v2__image"})
playlist_cover = playlist_cover["srcset"]

art_covers = re.findall('http[s]?://(?:[a-zA-Z]|[0-9]|[$-_@.&+]|[!*\(\),]|(?:%[0-9a-fA-F][0-9a-fA-F]))+',
                        playlist_cover)
contents = json.loads(results.renderContents())
playlist_name = contents["name"]
description = "".join(contents["description"].split("\n"))
tracks = contents["track"]
tracks = [track["name"] for track in tracks]
songs = [(''.join(filter(None, song.text.split('\n')))).strip() for song in song_results]
artists = [(song.replace(track, "")) for track, song in zip(tracks, songs)]
data = {"tracks": tracks, "artists": artists}
for _, art_cover in enumerate(art_covers):
    filename = art_cover.split("/")[-1]
    print(f"downloading album art {filename}.jpg ")
    wget.download(art_cover, f"artcover/{filename}.jpeg")

print(f"Album art download {filename}.jpg Complete ....")
### Start
create_playlist(playlist_name, description)
tracks = get_tracks(data)
playlist_id = get_playlist_id(playlist_name)

add_tracks_to_playlist(playlist_id, tracks)
